package bibCreator;

public class Article {
	private String author, journal, title, year, number, volume, pages, keywords, doi, ISSN, month;

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	public String getJournal() {
		return journal;
	}

	public void setJournal(String journal) {
		this.journal = journal;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public String getVolume() {
		return volume;
	}

	public void setVolume(String volume) {
		this.volume = volume;
	}

	public String getPages() {
		return pages;
	}

	public void setPages(String pages) {
		this.pages = pages;
	}

	public String getKeywords() {
		return keywords;
	}

	public void setKeywords(String keywords) {
		this.keywords = keywords;
	}

	public String getDoi() {
		return doi;
	}

	public void setDoi(String doi) {
		this.doi = doi;
	}

	public String getISSN() {
		return ISSN;
	}

	public void setISSN(String iSSN) {
		ISSN = iSSN;
	}

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	@Override
	public String toString() {
		return "Article [author=" + author + ", journal=" + journal + ", title=" + title + ", year=" + year
				+ ", number=" + number + ", volume=" + volume + ", pages=" + pages + ", keywords=" + keywords + ", doi="
				+ doi + ", ISSN=" + ISSN + ", month=" + month + ", getAuthor()=" + getAuthor() + ", getJournal()="
				+ getJournal() + ", getTitle()=" + getTitle() + ", getYear()=" + getYear() + ", getNumber()="
				+ getNumber() + ", getVolume()=" + getVolume() + ", getPages()=" + getPages() + ", getKeywords()="
				+ getKeywords() + ", getDoi()=" + getDoi() + ", getISSN()=" + getISSN() + ", getMonth()=" + getMonth()
				+ ", getClass()=" + getClass() + ", hashCode()=" + hashCode() + ", toString()=" + super.toString()
				+ "]";
	}
}
